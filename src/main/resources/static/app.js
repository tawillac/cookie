var stompClient = null;

function setConnected(connected) {
    $("#connect").prop("disabled", connected);
    $("#disconnect").prop("disabled", !connected);
    if (connected) {
        $("#conversation").show();
    }
    else {
        $("#conversation").hide();
    }
    $("#greetings").html("");
}

function connect() {
    console.log("connect -> cookie")
    var socket = new SockJS('/cookie');
    stompClient = Stomp.over(socket);
    stompClient.connect({}, function (frame) {
        setConnected(true);
        console.log('Connected: ' + frame);
        stompClient.subscribe('/topic/recipes', function (recipe) {
            showRecipe(JSON.parse(recipe.body).name + "/" + JSON.parse(recipe.body).ingredients + "/" + JSON.parse(recipe.body).instructions);
        });
    });
}

function disconnect() {
    if (stompClient !== null) {
        stompClient.disconnect();
    }
    setConnected(false);
    console.log("Disconnected");
}

function sendName() {
    stompClient.send("/app/request", {}, JSON.stringify({'recipeName': $("#name").val()}));
}

function showRecipe(recipe) {
    console.log("### -> " + recipe);
    $("#recipes").append("<tr><td>" + recipe + "</td></tr>");
}

$(function () {
    $("form").on('submit', function (e) {
        e.preventDefault();
    });
    $( "#connect" ).click(function() { connect(); });
    $( "#disconnect" ).click(function() { disconnect(); });
    $( "#send" ).click(function() { sendName(); });
});